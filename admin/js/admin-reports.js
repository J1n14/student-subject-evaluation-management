let reportsProfile = null;

async function initAdminReports(content, profile) {
  reportsProfile = profile;
  content.innerHTML = `
    <div class="section-card no-print">
      <h6 class="mb-3"><i class="bi bi-file-earmark-bar-graph me-1"></i>Generate Report</h6>
      <div class="row g-2 align-items-end">
        <div class="col-md-4">
          <label class="form-label small">Report Type</label>
          <select class="form-select" id="report-type">
            <option value="summary">Summary Overview</option>
            <option value="assignments">Subject Assignments</option>
            <option value="evaluations">Evaluation Results</option>
          </select>
        </div>
        <div class="col-md-3">
          <button class="btn btn-primary w-100" onclick="generateReport()"><i class="bi bi-eye me-1"></i>Generate</button>
        </div>
        <div class="col-md-3">
          <button class="btn btn-outline-secondary w-100" onclick="window.print()"><i class="bi bi-printer me-1"></i>Print</button>
        </div>
      </div>
    </div>
    <div id="report-output"></div>`;

  await generateReport();
}

async function generateReport() {
  const type = document.getElementById("report-type").value;
  const out = document.getElementById("report-output");
  out.innerHTML = `<div class="text-muted small">Generating...</div>`;

  const [studentsSnap, subjectsSnap, assignSnap, evalSnap] = await Promise.all([
    db.collection("students").get(),
    db.collection("subjects").get(),
    db.collection("studentSubjects").get(),
    db.collection("evaluations").get()
  ]);

  const students = studentsSnap.docs.map((d) => ({ id: d.id, ...d.data() }));
  const subjects = {};
  subjectsSnap.forEach((d) => (subjects[d.id] = d.data()));
  const assignments = assignSnap.docs.map((d) => d.data());
  const evaluations = evalSnap.docs.map((d) => d.data());

  const evaluatedCount = students.filter((s) => s.status === "Evaluated").length;
  const pendingCount = students.length - evaluatedCount;

  const header = `
    <div class="report-header text-center mb-4">
      <h4 class="mb-0">Nexus Integrative University</h4>
      <div class="text-muted">Student Subject Evaluation Management System</div>
      <div class="text-muted">Official System Report</div>
      <hr/>
      <div class="d-flex justify-content-between small text-muted">
        <span>Date Generated: ${new Date().toLocaleString()}</span>
        <span>Generated By: ${escapeHtml(reportsProfile.fullName || reportsProfile.email)} (Admin)</span>
      </div>
    </div>`;

  let body = "";

  if (type === "summary") {
    body = `
      <div class="row g-3 mb-4">
        ${[
          ["Total Students", students.length],
          ["Evaluated Students", evaluatedCount],
          ["Pending Students", pendingCount],
          ["Total Subjects", subjectsSnap.size],
          ["Total Evaluations", evalSnap.size]
        ]
          .map(([label, val]) => `<div class="col"><div class="border rounded p-3 text-center"><div class="small text-muted">${label}</div><h4 class="mb-0">${val}</h4></div></div>`)
          .join("")}
      </div>
      <table class="table table-bordered table-sm">
        <thead><tr><th>Student ID</th><th>Full Name</th><th>Course</th><th>Year</th><th>Status</th></tr></thead>
        <tbody>${students
          .map((s) => `<tr><td>${escapeHtml(s.id)}</td><td>${escapeHtml(s.fullName)}</td><td>${escapeHtml(s.course)}</td><td>${escapeHtml(s.yearLevel)}</td><td>${escapeHtml(s.status || "Pending")}</td></tr>`)
          .join("")}</tbody>
      </table>`;
  } else if (type === "assignments") {
    body = `
      <table class="table table-bordered table-sm">
        <thead><tr><th>Student ID</th><th>Student Name</th><th>Subject Code</th><th>Subject Name</th><th>Assigned At</th></tr></thead>
        <tbody>${assignments
          .map((a) => {
            const s = students.find((st) => st.id === a.studentId);
            const sub = subjects[a.subjectId];
            return `<tr><td>${escapeHtml(a.studentId)}</td><td>${escapeHtml(s ? s.fullName : "?")}</td><td>${escapeHtml(sub ? sub.subjectCode : "?")}</td><td>${escapeHtml(sub ? sub.subjectName : "?")}</td><td>${formatDate(a.assignedAt)}</td></tr>`;
          })
          .join("")}</tbody>
      </table>`;
  } else if (type === "evaluations") {
    body = `
      <table class="table table-bordered table-sm">
        <thead><tr><th>Student ID</th><th>Student Name</th><th>Subject</th><th>Status</th><th>Remarks</th><th>Evaluated By</th><th>Date</th></tr></thead>
        <tbody>${evaluations
          .map((e) => {
            const s = students.find((st) => st.id === e.studentId);
            const sub = subjects[e.subjectId];
            return `<tr><td>${escapeHtml(e.studentId)}</td><td>${escapeHtml(s ? s.fullName : "?")}</td><td>${escapeHtml(sub ? sub.subjectCode : "?")}</td><td>${escapeHtml(e.status)}</td><td>${escapeHtml(e.remarks)}</td><td>${escapeHtml(e.evaluatedBy)}</td><td>${formatDate(e.evaluatedAt)}</td></tr>`;
          })
          .join("")}</tbody>
      </table>`;
  }

  out.innerHTML = `<div class="table-responsive-card">${header}${body}</div>`;
}
